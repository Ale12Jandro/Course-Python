#Definir variables en PYTHON donde se le asigna algún valor o datos
Nombre = "Alejandro"

print(Nombre)

x = 100
print(x)

#Case sensitive (Laas variables se pueden definir por las Mayúsculas o Minúsculas)
book = "Libro digital"
Book = "Libro digital"

print(book)
print(Book)

#Las variables se pueden definir de la siguiente manera pero no es muy recomendable
y, texto = (50, "I am Ironman")
print(y)
print(texto)

#Conventiones tu puedes definir la forma en que declaras las variables
book_name = "Soy robot" #Snake case
bookName = "Libro infantil" #Camel case
BookName = "Hola" #Pascal case
